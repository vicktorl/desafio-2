class operacion {
    constructor(num1, num2) {
        this.num1 = num1;
        this.num2 = num2;

    }
    suma() {
        return parseFloat(num1) + parseFloat(num2);
    }

    resta() {
        return parseFloat(num1) - parseFloat(num2);
    }

    multiplicar() {
        return parseFloat(num1) / parseFloat(num2);
    }

    dividir() {
        return parseFloat(num1) / parseFloat(num2);
    }
}